/// Helper to create a ProductModel with sensible defaults
ProductModel _createProduct({
  required String name,
  required String hindiName,
  required String category,
  required List<String> keywords,
  required double price,
  required double mrpPrice,
  required String unit,
  required String description,
  required Map<String, String> nutrition,
  required int stock,
  required String imageUrl,
  List<String>? tags,
  String? brand,
}) {
  return ProductModel(
    id: '',  // Will be generated by Firestore
    name: name,
    hindiName: hindiName,  // NEW
    description: description,
    price: MonetaryValue(price),
    originalPrice: MonetaryValue(mrpPrice),
    discountPercentage: MonetaryValue(100 - (price / mrpPrice * 100)),
    unit: unit,
    categoryId: category.toLowerCase(),
    category: category,
    subCategory: '',
    shopId: 'fufaji-store-1',  // Main Fufaji store
    shopName: 'Fufaji Store',
    imageUrl: imageUrl,
    images: [imageUrl],
    keywords: keywords,  // NEW
    mrpPrice: mrpPrice,  // NEW
    nutrition: nutrition,  // NEW
    rating: 4.5,
    tags: tags ?? [],
    brand: brand,
    barcode: '',
    isAvailable: true,
    stockQuantity: stock,
    district: 'Jaipur',
    createdAt: DateTime.now(),
    updatedAt: DateTime.now(),
  );
}
