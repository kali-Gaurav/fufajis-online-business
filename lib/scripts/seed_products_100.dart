/// - Sugar & Jaggery (6)
///
/// USAGE: Call seedProducts100(firestore) from your admin panel or setup screen
/// Example: await seedProducts100(FirebaseFirestore.instance);

import 'package:cloud_firestore/cloud_firestore.dart';
import '../models/product_model.dart';
import '../utils/monetary_value.dart';

Future<void> seedProducts100(FirebaseFirestore firestore) async {
  print('[Seed] Starting to seed 100 test products...');
  final batch = firestore.batch();
  final productsRef = firestore.collection('products');

  final products = _generate100Products();
  int batchCount = 0;

  for (final product in products) {
    final docRef = productsRef.doc();
    batch.set(docRef, product.toMap());
    batchCount++;

    // Firestore batch is limited to 500 writes
    if (batchCount % 100 == 0) {
      await batch.commit();
      print('[Seed] Committed $batchCount products...');
    }
  }

  // Commit any remaining
  if (batchCount % 100 != 0) {
    await batch.commit();
  }

  print('[Seed] ✅ Seeded $batchCount products successfully!');
}

