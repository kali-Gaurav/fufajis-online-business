-- 11. ROW LEVEL SECURITY (RLS) POLICIES
-- ============================================================================

-- Enable RLS
ALTER TABLE users ENABLE ROW LEVEL SECURITY;
ALTER TABLE shops ENABLE ROW LEVEL SECURITY;
ALTER TABLE products ENABLE ROW LEVEL SECURITY;
ALTER TABLE orders ENABLE ROW LEVEL SECURITY;
ALTER TABLE payments ENABLE ROW LEVEL SECURITY;
ALTER TABLE packing_tasks ENABLE ROW LEVEL SECURITY;
ALTER TABLE delivery_assignments ENABLE ROW LEVEL SECURITY;
ALTER TABLE coupons ENABLE ROW LEVEL SECURITY;

-- Users: Can only see their own profile
CREATE POLICY "Users can view own profile" ON users
  FOR SELECT USING (auth.uid()::text = firebase_uid);

-- Shops: Public read, owner can write
CREATE POLICY "Shops are public" ON shops
  FOR SELECT USING (true);

CREATE POLICY "Shop owners can update own shop" ON shops
  FOR UPDATE USING (auth.uid()::text = (
    SELECT firebase_uid FROM users WHERE users.id = owner_id
  ));

-- Products: Public read, owner can write
CREATE POLICY "Products are public" ON products
  FOR SELECT USING (true);

CREATE POLICY "Shop owners can manage products" ON products
  FOR UPDATE USING (auth.uid()::text = (
    SELECT firebase_uid FROM users WHERE users.id =
      (SELECT owner_id FROM shops WHERE shops.id = products.shop_id)
  ));

-- Orders: Customers see own, owners see shop's
CREATE POLICY "Customers see own orders" ON orders
  FOR SELECT USING (
    customer_id = (SELECT id FROM users WHERE firebase_uid = auth.uid()::text)
  );

CREATE POLICY "Shop owners see shop orders" ON orders
  FOR SELECT USING (
    shop_id IN (SELECT id FROM shops WHERE owner_id = (
      SELECT id FROM users WHERE firebase_uid = auth.uid()::text
    ))
  );

-- Riders see assigned delivery orders
CREATE POLICY "Riders see assigned orders" ON orders
  FOR SELECT USING (
    assigned_rider_id = (SELECT id FROM users WHERE firebase_uid = auth.uid()::text)
  );

-- Coupons: Public read only
CREATE POLICY "Coupons are public" ON coupons
  FOR SELECT USING (true);

-- Delivery assignments: Riders see own, orders see if assigned
CREATE POLICY "Riders see own assignments" ON delivery_assignments
  FOR SELECT USING (
    rider_id = (SELECT id FROM users WHERE firebase_uid = auth.uid()::text)
  );

